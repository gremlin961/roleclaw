#!/usr/bin/env node

/**
 * DISC Agent Blueprint Generator
 * 
 * Creates complete OpenClaw agent workspaces from templates and DISC profiles.
 * 
 * Usage:
 *   node create-agent.js --role=hr-specialist --name="Sarah" --output="./generated-agents"
 *   node create-agent.js --role=developer --name="Alex" --output="./generated-agents"
 */

const fs = require('fs');
const path = require('path');

// Configuration
const BASE_DIR = path.resolve(__dirname, '..');
const TEMPLATES_DIR = path.join(BASE_DIR, 'templates');
const DISC_PROFILES_DIR = path.join(BASE_DIR, 'disc-profiles');
const SKILLS_MAPPING_FILE = path.join(BASE_DIR, 'skills-mapping', 'role-skills.json');
const MODELS_MAPPING_FILE = path.join(BASE_DIR, 'models-mapping', 'role-models.json');

// Color codes for terminal output
const colors = {
  reset: '\x1b[0m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  magenta: '\x1b[35m',
  cyan: '\x1b[36m',
  red: '\x1b[31m'
};

function log(message, color = 'reset') {
  console.log(`${colors[color]}${message}${colors.reset}`);
}

/**
 * Load JSON file
 */
function loadJSON(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    return JSON.parse(content);
  } catch (error) {
    log(`Error loading ${filePath}: ${error.message}`, 'red');
    process.exit(1);
  }
}

/**
 * Load template file
 */
function loadTemplate(filePath) {
  try {
    return fs.readFileSync(filePath, 'utf8');
  } catch (error) {
    log(`Error loading template ${filePath}: ${error.message}`, 'red');
    process.exit(1);
  }
}

/**
 * Replace variables in template
 * Format: ${variable_name}
 */
function renderTemplate(template, variables) {
  return template.replace(/\$\{([^}]+)\}/g, (match, key) => {
    const value = variables[key];
    if (value === undefined) {
      log(`Warning: Missing variable "${key}" in template`, 'yellow');
      return match;
    }
    return value;
  });
}

/**
 * Generate list from array
 */
function generateList(items, format = 'bullet') {
  if (!items || items.length === 0) {
    return '  - None specified';
  }
  
  const prefix = format === 'number' ? (index) => `${index + 1}. ` : '- ';
  return items.map((item, index) => `${prefix}${item}`).join('\n');
}

