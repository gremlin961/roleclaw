/**
 * Generate skill recommendations
 */
function generateSkillRecommendations(role) {
  const skillsMapping = loadJSON(SKILLS_MAPPING_FILE);
  const roleData = skillsMapping[role];
  
  if (!roleData) {
    log(`No skill mapping found for role: ${role}`, 'yellow');
    return { recommended: [], optional: [], model: 'default', reasoning: 'No mapping available' };
  }
  
  return {
    recommended: roleData.recommended_skills,
    optional: roleData.optional_skills,
    model: roleData.model,
    reasoning: roleData.reasoning
  };
}

/**
 * Create agent workspace
 */
function createAgent(role, agentName, outputDir) {
  log(`\n${'='.repeat(60)}`, 'cyan');
  log(`DISC Agent Blueprint Generator`, 'magenta');
  log(`${'='.repeat(60)}\n`, 'cyan');
  
  // Load role definitions
  const roleDefinitions = loadJSON(path.join(DISC_PROFILES_DIR, 'role-definitions.json'));
  const discProfiles = {
    'd': loadJSON(path.join(DISC_PROFILES_DIR, 'D-dominance.json')),
    'i': loadJSON(path.join(DISC_PROFILES_DIR, 'I-influence.json')),
    's': loadJSON(path.join(DISC_PROFILES_DIR, 'S-steadiness.json')),
    'c': loadJSON(path.join(DISC_PROFILES_DIR, 'C-conscientiousness.json'))
  };
  
  // Validate role
  if (!roleDefinitions.roles[role]) {
    log(`Error: Unknown role "${role}"`, 'red');
    log(`Available roles: ${Object.keys(roleDefinitions.roles).join(', ')}`, 'yellow');
    process.exit(1);
  }
  
  const roleDef = roleDefinitions.roles[role];
  const primaryProfile = discProfiles[roleDef.primary_disc.toLowerCase()];
  const secondaryProfile = discProfiles[roleDef.secondary_disc.toLowerCase()];
  
  log(`Creating agent: ${agentName}`, 'green');
  log(`Role: ${roleDef.name}`, 'green');
  log(`DISC Profile: ${roleDef.primary_disc}+${roleDef.secondary_disc} (${primaryProfile.name} + ${secondaryProfile.name})`, 'green');
  
  // Get skill recommendations
  const skillData = generateSkillRecommendations(role);
  
  log(`\n${'─'.repeat(60)}`, 'cyan');
  log(`Skill Recommendations`, 'blue');
  log(`${'─'.repeat(60)}`, 'cyan');
  
  log(`\n${colors.cyan}Recommended Skills (review before installing):${colors.reset}`);
  skillData.recommended.forEach(skill => {
    log(`  ✓ ${skill}`);
  });
  
  log(`\n${colors.yellow}Optional Skills:${colors.reset}`);
  skillData.optional.forEach(skill => {
    log(`  ○ ${skill}`);
  });
  
  log(`\n${colors.magenta}Recommended Model:${colors.reset} ${skillData.model}`);
  log(`Reasoning: ${skillData.reasoning}`);
  
  // Create output directory
  const agentDir = path.join(outputDir, agentName.replace(/\s+/g, '-').toLowerCase());
  fs.mkdirSync(agentDir, { recursive: true });
  
  // Template variables
  const variables = {
    agent_name: agentName,
    generation_date: new Date().toISOString().split('T')[0],
    role: roleDef.name,
    primary_disc: roleDef.primary_disc,
    secondary_disc: roleDef.secondary_disc,
    primary_disc_name: primaryProfile.name,
    secondary_disc_name: secondaryProfile.name,
    personality_summary: roleDef.personality_summary,
    communication_style_tone: primaryProfile.communication_style.tone,
    communication_style_pacing: primaryProfile.communication_style.pacing,
    communication_style_conflict: primaryProfile.communication_style.conflict_approach,
    communication_style_feedback: primaryProfile.communication_style.feedback_style,
    strengths_list: generateList(primaryProfile.strengths),
    boundary_guidance: roleDef.soul_guidance?.boundaries_template || 'Maintain professional boundaries',
    work_rhythm_morning: 'Review priorities and communications',
    work_rhythm_afternoon: 'Focus on deep work and execution',
    work_rhythm_evening: 'Document progress and plan next steps',
    soul_identity_template: roleDef.soul_guidance?.identity_template || 'You bring the DISC-driven traits to every interaction.',
    soul_communication_template: roleDef.soul_guidance?.communication_template || 'Your communication style reflects your DISC profile.',
    soul_boundaries_template: roleDef.soul_guidance?.boundaries_template || 'You maintain clear professional boundaries.',
    soul_work_rhythm_template: roleDef.soul_guidance?.work_rhythm_template || 'You follow a structured daily rhythm.',
    model: skillData.model,
    reasoning: skillData.reasoning
  };
  
  // Process templates
  const templatesDir = path.join(TEMPLATES_DIR, role);
  const templateFiles = [
    'SOUL.md.template',
    'MEMORY.md.template',
    'TOOLS.md.template',
    'AGENTS.md.template',
    'HEARTBEAT.md.template',
    'config.json.template'
  ];
  
  log(`\n${'─'.repeat(60)}`, 'cyan');
  log(`Generating Workspace Files`, 'blue');
  log(`${'─'.repeat(60)}`, 'cyan');
  
  templateFiles.forEach(templateFile => {
    const templatePath = path.join(templatesDir, templateFile);
    const outputFile = templateFile.replace('.template', '');
    const outputPath = path.join(agentDir, outputFile);
    
    // Load and render template
    let content = loadTemplate(templatePath);
    content = renderTemplate(content, variables);
    
    // Write file
    fs.writeFileSync(outputPath, content);
    log(`  ✓ ${outputFile}`, 'green');
  });
  
  // Create memory directory
  const memoryDir = path.join(agentDir, 'memory');
  fs.mkdirSync(memoryDir, { recursive: true });
  log(`  ✓ memory/ (directory created)`, 'green');
  
  // Create README
  const readmeContent = `# ${agentName} - ${roleDef.name} Agent

**Generated:** ${variables.generation_date}

## Overview

${agentName} is a ${roleDef.name} agent built with the DISC Agent Blueprint System.

**DISC Profile:** ${roleDef.primary_disc}+${roleDef.secondary_disc} (${primaryProfile.name} + ${secondaryProfile.name})

**Description:** ${roleDef.personality_summary}

## Quick Start

1. **Review the generated files:**
   - \`SOUL.md\` - Personality and behavior guide
   - \`MEMORY.md\` - Long-term memory structure
   - \`TOOLS.md\` - Tools and skills configuration
   - \`AGENTS.md\` - Workspace protocols
   - \`HEARTBEAT.md\` - Periodic check tasks
   - \`config.json\` - Agent configuration

2. **Install recommended skills:**
   ${skillData.recommended.map(s => `- ${s}`).join('\n   ')}

3. **Deploy to OpenClaw:**
   - Copy this workspace to your OpenClaw instance
   - Configure the agent with the recommended model
   - Enable the recommended skills

## Next Steps

- Review and approve the skill recommendations
- Customize the templates to match your organization
- Test the agent in a controlled environment
- Deploy to production when ready

---

*Generated by DISC Agent Blueprint System v0.1.0*
`;
  
  fs.writeFileSync(path.join(agentDir, 'README.md'), readmeContent);
  log(`  ✓ README.md`, 'green');
  
  // Summary
  log(`\n${'='.repeat(60)}`, 'cyan');
  log(`Agent Workspace Created Successfully!`, 'green');
  log(`${'='.repeat(60)}`, 'cyan');
  log(`\nLocation: ${agentDir}`, 'cyan');
  log(`\nNext Steps:`, 'yellow');
  log(`  1. Review the generated files in ${agentDir}`, 'yellow');
  log(`  2. Install the recommended skills (manual approval required)`, 'yellow');
  log(`  3. Customize templates as needed`, 'yellow');
  log(`  4. Deploy to your OpenClaw instance`, 'yellow');
  log(`\n${'='.repeat(60)}\n`, 'cyan');
  
  return {
    success: true,
    agentDir,
    skillData,
    roleDef
  };
}

/**
 * Parse command line arguments
 */
function parseArgs(args) {
  const options = {
    role: null,
    name: null,
    output: './generated-agents',
    help: false
  };
  
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    
    if (arg === '--help' || arg === '-h') {
      options.help = true;
    } else if (arg.startsWith('--role=')) {
      options.role = arg.split('=')[1];
    } else if (arg.startsWith('--name=')) {
      options.name = arg.split('=')[1];
    } else if (arg.startsWith('--output=')) {
      options.output = arg.split('=')[1];
    }
  }
  
  return options;
}

